document.addEventListener('DOMContentLoaded', () => {
    const fromCurrency = document.getElementById('fromCurrency');
    const toCurrency = document.getElementById('toCurrency');

    for (let currency in currencyList) {
        let optionFrom = document.createElement('option');
        optionFrom.value = currency;
        optionFrom.textContent = `${currency} - ${currencyList[currency]}`;
        fromCurrency.appendChild(optionFrom);

        let optionTo = document.createElement('option');
        optionTo.value = currency;
        optionTo.textContent = `${currency} - ${currencyList[currency]}`;
        toCurrency.appendChild(optionTo);
    }
});

async function convertCurrency() {
    const amount = document.getElementById('amount').value;
    const fromCurrency = document.getElementById('fromCurrency').value;
    const toCurrency = document.getElementById('toCurrency').value;
    const resultDiv = document.getElementById('result');

    if (!amount || isNaN(amount) || amount <= 0) {
        resultDiv.textContent = 'Please enter a valid amount';
        return;
    }

    try {
        const response = await fetch(`https://api.exchangerate-api.com/v4/latest/${fromCurrency}`);
        const data = await response.json();
        const rate = data.rates[toCurrency];
        const result = (amount * rate).toFixed(2);

        resultDiv.textContent = `${amount} ${fromCurrency} = ${result} ${toCurrency}`;
    } catch (error) {
        resultDiv.textContent = 'Error fetching exchange rate data';
    }
}
